package models;

public class Related {

	private int rel_no;
	private String rel_name;
	private String rel_brief;
	private String rel_photo;

	public String getRel_name() {
		return rel_name;
	}

	public void setRel_name(String rel_name) {
		this.rel_name = rel_name;
	}

	public String getRel_brief() {
		return rel_brief;
	}

	public void setRel_brief(String rel_brief) {
		this.rel_brief = rel_brief;
	}

	public int getRel_no() {
		return rel_no;
	}

	public void setRel_no(int rel_no) {
		this.rel_no = rel_no;
	}

	public String getRel_photo() {
		return rel_photo;
	}

	public void setRel_photo(String rel_photo) {
		this.rel_photo = rel_photo;
	}

	
}
