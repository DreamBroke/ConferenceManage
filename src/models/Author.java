package models;

public class Author {

	private int aut_no;
	private String aut_category;
	private String aut_name;
	private String aut_tel;
	private String aut_email;
	private String aut_company;
	private int aut_dissertation;
	public int getAut_no() {
		return aut_no;
	}
	public void setAut_no(int aut_no) {
		this.aut_no = aut_no;
	}
	public String getAut_category() {
		return aut_category;
	}
	public void setAut_category(String aut_category) {
		this.aut_category = aut_category;
	}
	public String getAut_name() {
		return aut_name;
	}
	public void setAut_name(String aut_name) {
		this.aut_name = aut_name;
	}
	public String getAut_tel() {
		return aut_tel;
	}
	public void setAut_tel(String aut_tel) {
		this.aut_tel = aut_tel;
	}
	public String getAut_email() {
		return aut_email;
	}
	public void setAut_email(String aut_email) {
		this.aut_email = aut_email;
	}
	public String getAut_company() {
		return aut_company;
	}
	public void setAut_company(String aut_company) {
		this.aut_company = aut_company;
	}
	public int getAut_dissertation() {
		return aut_dissertation;
	}
	public void setAut_dissertation(int aut_dissertation) {
		this.aut_dissertation = aut_dissertation;
	}
	
}
